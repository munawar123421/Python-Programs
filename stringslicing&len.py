
name="munawar"
name1="munawar ali khan"
#length of string in python
print(len(name))
print(len(name1))
#string slicing in python
print(name[0:2]) #first is included but second is not included
print(name[0:6])
print(name[0:]) #python take bydefault value is total length of string
print(name[:4])
print(name1)
print(name1[0:16])
print(name1[:10]) #positive slicing
print(name1[0:-2])#negative slicing
print(name1[-10:-1])
print(name1[len(name1)-10:len(name1)-2])#python interprator automatically do it.
print(name[:])
print(name[2:])

