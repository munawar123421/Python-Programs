#Typecasting in python
#explicit typecasting done by programmer
#example no.1
a="1"
b="2"
print("result for explicit typecasting:")
print(int(a)+int(b))
#example no.2
a=12
b="12.4"
c=float(b)
print(a+int(c))
#implicit typecasting done by python interprator
#example no.1
var1=10.99
var2=20     #converting smaller data type like int to higher data type like float
print("result for implicit typecasting:")
print(var1+var2)
#example no.2
var1=22
var2=22.5
print(var1+var2)




